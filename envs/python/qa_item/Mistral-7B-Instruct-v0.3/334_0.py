from datetime import date

def calculate_good_friday(year: int) -> str:
    # Easter Sunday calculation based on the Gregorian method
    a = year % 19
    b = year // 100
    c = year % 100
    d = b // 4
    e = b % 4
    f = (b + 8) // 25
    g = (b - f + 1) // 3
    h = (19 * a + b - d - g + 15) % 30
    k = c // 100
    i = c % 100
    l = (32 + 2 * e + 2 * i - h - k) % 7
    m = (a + 11 * h + 22 * l) // 451
    day = (h + l - 7 * m + 114) % 31
    month = (h + l - 7 * m + 2) // 31
    month = 3 + month

    # Adjust for the century
    if month > 3:
        year += 1
        month -= 4

    # Calculate Good Friday
    good_friday = date(year, month, day).strftime('%a %b %d %Y')
    return good_friday
