from decimal import Decimal, getcontext

def compute_pi(digits: int) -> str:
    """
    Computing PI (π) Using Gauss-Legendre Algorithm with High-precision Arithmetic Library.

    Args:
        digits (int): The number of decimal digits to compute π to.

    Returns:
        str: The value of π to the specified number of digits.
    """
    getcontext().prec = digits + 2  # extra digits for intermediate calculations
    a = Decimal(1)
    b = Decimal(1) / Decimal(2).sqrt()
    t = Decimal(1) / Decimal(4)
    p = Decimal(1)
    pi = b

    while True:
        a_next = (a + b) / 2
        b = (a * b).sqrt()
        t -= p * (a - a_next) ** 2
        a = a_next
        p *= 2
        pi_next = (a + b) ** 2 / (4 * t)
        if pi == pi_next:
            break
        pi = pi_next

    return str(pi)[:digits + 2]  # include the decimal point
