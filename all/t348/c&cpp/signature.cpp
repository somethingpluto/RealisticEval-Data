/**
 * @brief Finds all prime numbers within a specified range.
 *
 * @param lower_bound The lower limit of the range (inclusive).
 * @type lower_bound int
 *
 * @param upper_bound The upper limit of the range (inclusive).
 * @type upper_bound int
 *
 * @return A vector containing all prime numbers within the specified range.
 * @rtype std::vector<int>
 */
std::vector<int> find_primes(int lower_bound, int upper_bound);